public class OperadoresUnarios {

    public static void main(String[] args) {
        
        int i = -5;
        
        int j = +i;    // + x -  = -
        
        int k = -i;  //  -  X -  = +
        System.out.println("j = " + j);//
        System.out.println("i = " + i);//
        System.out.println("k = " + k);//
    }
}
